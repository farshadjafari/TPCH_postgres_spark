BEGIN;
\O 6.SQL.OUT
\TIMING ON
SELECT
	SUM(L_EXTENDEDPRICE * L_DISCOUNT) AS REVENUE
FROM
	LINEITEM
WHERE
	L_SHIPDATE >= DATE '1993-01-01'
	AND L_SHIPDATE < DATE '1993-01-01' + INTERVAL '1' YEAR
	AND L_DISCOUNT BETWEEN 0.04 - 0.01 AND 0.04 + 0.01
	AND L_QUANTITY < 25
;
COMMIT;
